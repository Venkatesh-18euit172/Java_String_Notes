package stringclass;

import java.util.Locale;

public class M4_12_toLowerCase {

	public static void main(String[] args) {
		
      String s1="VENKATESH";
      System.out.println("Using toLowerCase :- "+s1.toLowerCase());
      
      System.out.println("Using toLowerCase(Locale l) :- "+s1.toLowerCase(Locale.ENGLISH));
      
	}

}
