package stringclass;

import java.util.Locale;

public class M4_13_toUpperCase {

	public static void main(String[] args) {
		
      String s1="venkatesh";
      System.out.println("Using toUpperCase :- "+s1.toUpperCase());
      
      System.out.println("Using toUpperCase(Locale l) :- "+s1.toUpperCase(Locale.ENGLISH));

	}

}
