package stringclass;

public class M5_17_startsWith {

	public static void main(String[] args) {
        
        String s1="venkatesh";
        System.out.println("Using startsWith(String s) :- "+s1.startsWith("ven"));
        
        System.out.println("Using startsWith(String s,int startindex) :- "+s1.startsWith("kat",3));
	}

}
